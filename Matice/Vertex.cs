namespace ComputerGraphics2D
{
	public class Vertex
	{
		public float X;
		public float Y;

		/// <summary>
		/// Vertex
		/// </summary>
		public Vertex()
		{
			X = 0;
			Y = 0;
		}

		/// <summary>
		/// Vertex
		/// </summary>
		/// <param name="x"></param>
		/// <param name="y"></param>
		public Vertex(float x, float y)
		{
			X = x;
			Y = y;
		}
	}
}
